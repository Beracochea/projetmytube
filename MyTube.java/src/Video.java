
public class Video {
	private String author_title;
	private int numberOfViews;
	private int numberOfLikes;
	private int numberOfDislikes;
	private String share;
	private String addTo;
	private String report;
	private String addToTranslations;
	private int subscribe;
	
	public String getAuthor_title() {
		return author_title;
	}
	public void setAuthor_title(String author_title) {
		this.author_title = author_title;
	}
	public int getNumberOfViews() {
		return numberOfViews;
	}
	public void setNumberOfViews(int numberOfViews) {
		this.numberOfViews = numberOfViews;
	}
	public int getNumberOfLikes() {
		return numberOfLikes;
	}
	public void setNumberOfLikes(int numberOfLikes) {
		this.numberOfLikes = numberOfLikes;
	}
	public int getNumberOfDislikes() {
		return numberOfDislikes;
	}
	public void setNumberOfDislikes(int numberOfDislikes) {
		this.numberOfDislikes = numberOfDislikes;
	}
	public String getShare() {
		return share;
	}
	public void setShare(String share) {
		this.share = share;
	}
	public String getAddTo() {
		return addTo;
	}
	public void setAddTo(String addTo) {
		this.addTo = addTo;
	}
	public String getReport() {
		return report;
	}
	public void setReport(String report) {
		this.report = report;
	}
	public String getAddToTranslations() {
		return addToTranslations;
	}
	public void setAddToTranslations(String addToTranslations) {
		this.addToTranslations = addToTranslations;
	}
	public int getSubscribe() {
		return subscribe;
	}
	public void setSubscribe(int subscribe) {
		this.subscribe = subscribe;
	}

}
